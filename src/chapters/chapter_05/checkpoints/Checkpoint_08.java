package chapters.chapter_05.checkpoints;

public class Checkpoint_08 {
	
	public static void main(String[] args) {
	
		int sum = 0;
		int summ = 0;
		
		for (int i = 0; i < 10; ++i) {
			sum += i;
			}
		System.out.println(sum);
		
		for (int i = 0; i < 10; i++) {
			summ += i;
			}
		System.out.println(summ);
	}

}
// yes they are the same